<h2>Mutui</h2>
<p class="mt-1"> 
    Il mutuo è un contratto mediante il quale una parte, detta mutuante, consegna all’altra, detta mutuataria, una somma di denaro o una quantità  di beni fungibili, che l’altra si obbliga a restituire successivamente con altrettante cose della stessa specie e qualità.<br>
    <br>
    Una tra le figure più diffuse nella prassi è il mutuo immobiliare, concesso per soddisfare esigenze di denaro in qualche modo collegate all’acquisto di un bene immobile per compravendita (o, nel caso di fabbricati, anche per costruzione).<br>
    <br>
    Una particolare forma di mutuo immobiliare è il mutuo fondiario: si tratta di un mutuo immobiliare con particolari caratteristiche di durata (superiore a 18 mesi) e rapporto tra somma mutuata e valore della garanzia (non superiore all’80% secondo la legislazione vigente).<br>
    <br>
    In applicazione della precedente normativa sul credito fondiario, il mutuo fondiario poteva venir concesso soltanto da alcuni (pochi) istituti di credito espressamente autorizzati, che svolgevano tale attività in via principale o disponevano al loro interno di una apposita Sezione Autonoma di Credito Fondiario (SACF); in seguito, anche in considerazione della crescente presenza di operatori stranieri sul mercato specifico, tale forma tecnica è stata consentita a tutti gli intermediari finanziari.<br>
    <br>
    L’attività di ricerca e mediazione tra banca e cliente viene svolta dal mediatore creditizio.<br>
</p>

