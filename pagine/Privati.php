<h2 class="mt-3">Prestiti Personali</h2>
<p class="mt-1"> 
    Il prestito è la cessione di una somma di denaro con il vincolo della restituzione di capitali di pari valore o maggiori.<br>
    <br>
    Il termine indica essenzialmente un finanziamento di denaro che un istituto o società di credito autorizzata (es. banca) concede ad un altro soggetto economico.<br>
    <br>
    <strong>Caratteristiche</strong><br>
    Gli elementi costitutivi di un prestito sono:<br>
    <ul class="list-group">
        <li class="list-group-item list-group-item-info">capitale finanziato
        <li class="list-group-item list-group-item-info">tasso annuo nominale d’interesse (TAN)
        <li class="list-group-item list-group-item-info">tasso annuo effettivo globale (TAEG)
        <li class="list-group-item list-group-item-info">durata del finanziamento
        <li class="list-group-item list-group-item-info">l’importo, ed eventuali rate e condizioni.
    </ul>
    <br>
    L’assegnazione di un prestito avviene dopo una serie di controlli preliminari che il mediatore esegue in base alla situazione economica e professionale del soggetto richiedente, esami che gli permettono di valutare la sicurezza evitando sconvenienti situazioni di insolvenza.<br>
</p>


