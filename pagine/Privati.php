<h2>CQS</h2>
<p class="mt-1"> 
    La cessione del quinto dello stipendio è una particolare tipologia di prestito personale previsto in Italia, da estinguersi con cessione di quote dello stipendio o salario fino al quinto dell’ammontare dell’emolumento valutato al netto di ritenute.<br>
</p>

<h2>Disciplina normativa</h2>
<p class="mt-1">    
    ntrodotta nel secondo dopoguerra, dal D.P.R. 5 gennaio 1950 n. 180, e dal relativo regolamento attuativo, il D.P.R. 28 luglio 1950, n. 895.<br>
</p>

<h2>Caratteristiche del prestito</h2>
<p class="mt-1"> 
    L’espressione cessione del quinto di stipendio deriva dal fatto che l’importo massimo della rata di rimborso del prestito non può superare il valore di 1/5 (cioè il 20%) dello stipendio mensile netto continuativo, inoltre la durata massima consentita è di 120 mesi e la minima abitualmente non è inferiore ai 24 mesi.<br>
    <br>
    Il termine massimo della durata non può eccedere comunque il termine del rapporto di lavoro e il pensionamento, tranne che per i dipendenti ministeriali, i quali possono decidere se estinguere il debito o traslarlo sulla pensione.<br>
    <br>
    Oggi è possibile l’accesso alla cessione anche da parte dei pensionati ed in questo caso la scadenza non può eccedere il 90° anno di età, anche se oggi nella prassi le compagnie di assicurazione limitano il rischio assumendo prodotti con un massimo di 85 anni. Tuttavia alcuni gruppi bancari, facendo ricorso al fondo previdenziale INPDAP riescono ad arrivare fino ad un massimo di 95 anni di età.<br>
    <br>
    La legge prevede che, al momento della stipula del contratto con la società  finanziaria, si stipuli anche un’assicurazione sui rischi vita ed impiego.<br>
    <br>
    Nel caso di “rischio impiego” l’assicurazione interviene, ma ha diritto di rivalsa nei confronti del debitore, nei limiti del TFR (Trattamento di fine rapporto) fino a quel momento maturato: tale cifra, accantonata dall’azienda in un apposito fondo, resta quindi indisponibile per il mutuatario che accede al finanziamento; si tratta quindi di un’assicurazione a vantaggio della finanziaria.<br>
    <br>
    Nel caso di “rischio vita”, l’assicurazione interviene senza vantare diritto di rivalsa nei confronti degli eredi.<br>
</p>