<h2>Finanziamenti Chirografari</h2>
<p class="mt-1"> 
    E’ destinato a tutte le imprese che intendono effettuare investimenti. A fronte dell’esigenza del cliente si eroga, in un’unica soluzione, una somma che il cliente si impegna a rimborsare secondo un piano di ammortamento prestabilito. La frequenza delle rate mensile. Ogni rata comprensiva di una quota capitale e di una quota interessi (ammortamento alla francese). Non è prevista l’estinzione parziale del debito, ma è ammessa la sua estinzione anticipata, previo pagamento della relativa commissione.<br>
    <br>
</p>

<h2>Finanziamenti Import/Export</h2>
<p class="mt-1"> 
    Operazioni mediante le quali la banca anticipa al cliente, in genere un’impresa, il ricavo di un credito commerciale vantato verso un soggetto residente all’estero (finanziamento export) oppure gli anticipa i fondi necessari per regolare a favore di un soggetto residente all’estero il pagamento di forniture o servizi (finanziamento import).<br>
    <br>
</p>
