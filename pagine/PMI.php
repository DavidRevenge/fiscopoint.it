</p>


<h2>Leasing</h2>
<p class="mt-1"> 
    Il leasing è un’operazione finanziaria che consente all’utilizzatore di avere la disponibilità di un bene, per un certo periodo di tempo, verso il corrispettivo di un canone periodico.<br>
    <br>
    Il leasing si puo’ distinguere nelle seguenti categorie:<br>
    <ul class="list-group">
        <li class="list-group-item list-group-item-info">Leasing Strumentale: per macchine utensili, impianti tecnico-produttivi, attrezzature, arredi
        <li class="list-group-item list-group-item-info">Leasing Immobiliare: per immobili costruiti e da costruire
        <li class="list-group-item list-group-item-info">Leasing automobilistico: per autoveicoli
        <li class="list-group-item list-group-item-info">Leasing navale: per navi e imbarcazioni da diporto
    </ul>
</p>

<h2>Confidi</h2>
<p class="mt-1"> 
    Confidi, è un consorzio italiano che svolge attività  di prestazione di garanzie per agevolare le imprese nell’accesso ai finanziamenti, a breve, medio e lungo termine, destinati alle attività  economiche e produttive.<br>
    <br>
    I confidi sono disciplinati dal Testo Unico Bancario (TUB) D.Lgs. 385 del 1993.<br>
    <br>
    I confidi nascono come espressione delle associazioni di categoria nei comparti dell’industria, del commercio, dell’artigianato e dell’agricoltura, basandosi su principi di mutualità e solidarietà. I primi consorzi fidi, o cooperative di garanzia, vengono costituiti già  nel 1956 per facilitare l’accesso al credito alle piccole imprese.<br>
</p>


<h2>Factoring</h2>
<p class="mt-1"> 
    Il Factoring consiste nella cessione di crediti commerciali a breve termine, originati da vendite di merci o da prestazioni di servizi, dal loro titolare originario ad una persona specializzata, detta factor (che in pratica è una società), la quale si assume l’incarico della riscossione e, contro il pagamento di una commissione aggiuntiva, ne garantisce anche il buon fine contro il pericolo di insolvenza momentanea o di fallimento del debitore.<br>
</p>

<h2>Cessione Del Credito Pro Soluto</h2>
<p class="mt-1"> 
    Quando il cedente non deve rispondere dell’eventuale inadempienza (solvibilità ) del debitore. Garantisce solamente dell’esistenza del credito.<br>
</p>

<h2>Cessione Del Credito Pro Solvendo</h2>
<p class="mt-1"> 
    Quando il cedente risponde dell’eventuale inadempienza del debitore.<br>
</p>

