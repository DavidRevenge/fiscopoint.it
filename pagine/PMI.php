    Consente di avere un’apertura di credito sul proprio conto corrente, che si esplicita nella possibilità per il cliente di utilizzare in qualsiasi momento ed in qualsiasi modo le somme messe a disposizione da parte dell’istituto di credito affidante, di norma senza preavviso e senza particolari obblighi di restituzione in termini temporali.<br>
