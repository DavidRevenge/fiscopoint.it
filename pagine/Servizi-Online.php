<p class="mt-1"> 
    <strong>La Carta Prepagata</strong><br>
    Una carta di debito ricaricabile in tutto il mondo nel circuito Mastercard. E’ prepagata ovvero ti consente di spendere solo il denaro precedentemente caricato. La si può ricaricare mensilmente fino a 30.000 € (100.000 Euro/anno). E’ abilitata solo all’utilizzo elettronico: le transazioni sono eseguite on-line ovvero l’importo viene immediatamente scalato dalla disponibilità  sulla carta.<br>
    <br>
    Ha un numero identificativo di 16 cifre stampate sul fronte ed una validità  di 5 anni. Il numero della carta viene riportato anche sul retro ove presente 3 cifre aggiuntive che sono il cosiddetto cv2: un codice di sicurezza che viene richiesto per la transazione su Internet. Ha un proprio numero segreto (PIN) di 5 cifre fornito insieme al kit di attivazione, necessario per prelevare presso tutti gli sportelli automatici (ATM) ed utilizzarla per i tuoi acquisti presso tutti i negozi che hanno un pos convenzionato mastercard. Nel caso di furto/smarrimento della carta, il credito residuo, potrà  essere recuperato previa procedura di blocco.<br>
    <br>
</p>


