        <p>Sul Modello 730, il Caf rilascia <b>il visto di conformità (ossia una certificazione di correttezza dei dati)</b> che, garantisce il contribuente nei confronti dell’Agenzia delle Entrate, con particolare riferimento alle spese che danno diritto a determinate benefici fiscali (spese sanitarie, interessi passivi, spese per recupero patrimonio edilizio, Superbonus, ecc.).</p>
    <h3 style="color:blue">Accesso al 730 Precompilato</h3>
        <p>Per accedere al 730 Precompilato il Caf deve essere appositamente delegato dal contribuente, a tale scopo il Caf Lavoro e Fisco ha predisposto un apposito modello di delega che, dovrà essere compilata, firmata e consegnata al Caf (Delega Unica).<!--mettere collegamento CON IL MODELLO DELLA DELEGA--></p>
    <h3 style="color:blue">Quando può essere presentato il 730</h3>
        <p>Il Modello 730 può essere presentato entro il 30 settembre di ogni anno con riferimento ai redditi dell’anno precedente. Ad esempio: per i redditi percepiti nell’anno 2020 il 730 può essere presentato al Caf entro il 30 settembre 2021.</p>
    <h3 style="color:blue">Pagamenti e rimborsi</h3>
        <p>Se dal Modello 730 emerge un credito da rimborsare, il contribuente otterrà il rimborso direttamente dal datore di lavoro o dall'ente pensionistico. Se, invece, emerge un debito, il datore di lavoro o l'ente pensionistico effettuerà la trattenuta. La somma sarà accreditata (o trattenuta) nella busta paga o nella rata di pensione a partire, rispettivamente, da luglio e agosto/settembre.</p>
    <h3 style="color:blue">Modello 730 senza sostituto di imposta</h3>
        <p>I contribuenti che non hanno un sostituto d'imposta, per esempio perché hanno perso il lavoro nel corso dell'anno, possono presentare il 730 e ricevono l'eventuale rimborso direttamente dall'Agenzia delle Entrate.</p>
        <p>Se il contribuente ha fornito all'Agenzia le coordinate del suo conto corrente bancario o postale (codice Iban), il rimborso viene accreditato su quel conto.</p>
        <p>Se, al contrario, emerge un debito, il contribuente può effettuare il versamento direttamente con il modello F24 che gli sarà consegnato dal Caf.</p>
        <p><b>Il Modello 730 può essere utilizzato anche dagli eredi per conto delle persone decedute.</b></p>
