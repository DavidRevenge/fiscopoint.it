    <h2 style="color:blue">Assegno di maternità dei Comuni</h2>
    <h3 style="color:blue">Che cosè</h3>
    <p>E’ un assegno che la madre non lavoratrice può chiedere al proprio Comune di residenza per la nascita del figlio oppure per l’adozione o l’affidamento preadottivo di un minore di età non superiore ai 6 anni (o ai 18 anni in caso di adozioni o affidamenti internazionali).</p>
    <p>La madre lavoratrice può chiedere l’assegno se non ha diritto all’indennità di maternità dell’Inps oppure alla retribuzione per il periodo di maternità. Se l’importo dell’indennità o della retribuzione è inferiore all’importo dell’assegno, la madre lavoratrice può chiedere al Comune l’assegno in misura ridotta.</p>
    <h3 style="color:blue">A CHI SPETTA</h3>
    <p>Cittadine italiane o comunitarie residenti in Italia al momento del parto o ingresso in famiglia del minore adottato/affidato;</p>
    <p>cittadine non comunitarie residenti in Italia al momento del parto o ingresso in famiglia del minore adottato/affidato in possesso di uno dei seguenti titoli di soggiorno:</p>
        <ul>    
            <li>carta di soggiorno;</li>
            <li>permesso di soggiorno CE per soggiornanti di lungo periodo.</li>
        </ul>
    <p>Il figlio di cittadina non comunitaria nato all’estero deve essere in possesso dello stesso titolo di soggiorno della madre.</p> 
    <p>In alcuni casi particolari, se la madre non può richiedere l’assegno, il beneficio può essere richiesto, a seconda dei casi, dal padre del bambino, dal genitore della madre, dall’adottante, dall’affidatario preadottivo o dall’affidatario non preadottivo.</p>
